package part_03_02_comparison_operators;

public class ComparisonOperators {
    public static void main(String[] args) {

//        x == y
        System.out.println("5 == 6 -> " + (5 == 6));
        System.out.println("5 == 5 -> " + (5 == 5));
        System.out.println();

//        x != y
        System.out.println("5 != 6 -> " + (5 != 6));
        System.out.println("5 != 5 -> " + (5 != 5));
        System.out.println();

//        x < y
        System.out.println("5 < 6 -> " + (5 < 6));
        System.out.println("6 < 5 -> " + (6 < 5));
        System.out.println();

//        x <= y
        System.out.println("5 <= 6 -> " + (5 <= 6));
        System.out.println("5 <= 5 -> " + (5 <= 5));
        System.out.println("6 <= 5 -> " + (6 <= 5));
        System.out.println();

//        x > y
        System.out.println("5 > 6 -> " + (5 > 6));
        System.out.println("6 > 5 -> " + (6 > 5));
        System.out.println();

//        x >= y
        System.out.println("5 >= 6 -> " + (5 >= 6));
        System.out.println("5 >= 5 -> " + (5 >= 5));
        System.out.println("6 >= 5 -> " + (6 >= 5));
        System.out.println();

    }
}
